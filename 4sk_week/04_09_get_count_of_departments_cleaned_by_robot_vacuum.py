dr = [-1,0,1,0]
dc = [0,1,0,-1]

def get_count_of_departments_cleaned_by_robot_vacuum(r, c, d, room_map):
    room_map[r][c] = 2
    count = 1
    queue = deque([[r, c, d]])

    while queue:
        r, c, d = queue.popleft()
        tmp_d = d

        for i in range(4):
            tmp_d -= 1
            if tmp_d < 0:
                tmp_d = 3

            new_r, new_c = r + dr[tmp_d], c + dc[tmp_d]
            if room_map[new_r][new_c] == 0:
                room_map[new_r][new_c] = 2
                count += 1
                queue.append([new_r, new_c, tmp_d])
                break
            elif i == 3:
                new_r, new_c = r - dr[tmp_d], c - dc[tmp_d]
                queue.append([new_r, new_c, tmp_d])

                if room_map[new_r][new_c] == 1:
                    return count


